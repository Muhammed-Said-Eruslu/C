#include "libft.h"

char *ft_strdup(const char *s)
{
	char *dst;
	size_t len;
	size_t i;

	len = ft_strlen(s);
	dst = malloc(len + 1);
	if (!dst)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}